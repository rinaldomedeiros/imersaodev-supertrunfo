var cartaMaquina
var cartaJogador
var cartas = [carta1,carta2,carta3,carta4,carta5,carta6,carta7,carta8]
     
var vezDoJogador = true
var pontosJogador = 0
var pontosMaquina = 0
atualizaPlacar()
atualizaQuantidadeCartas()

function atualizaQuantidadeCartas(){
  var divQuantidadeCartas = document.getElementById('quantidade-cartas')
  var html = "Quantidade de cartas no jogo: " + cartas.length
  divQuantidadeCartas.innerHTML = html
}

function atualizaPlacar(){
  var divPlacar = document.getElementById('placar')
  var html = "Jogador " + pontosJogador + " x " + pontosMaquina + " Máquina"
  divPlacar.innerHTML = html
}

function sortearCarta() {
    var numeroCartaMaquina = parseInt(Math.random() * cartas.length)
    cartaMaquina = cartas[numeroCartaMaquina]
    cartas.splice(numeroCartaMaquina,1)
  
    var numeroCartaJogador = parseInt(Math.random() * cartas.length)
    cartaJogador = cartas[numeroCartaJogador]
    cartas.splice(numeroCartaJogador,1)
  
    document.getElementById('btnSortear').disabled = true
    document.getElementById('btnJogar').disabled = false
    
    exibeCartaJogador()
    verificaVezDeJogar()
}

function exibeCartaJogador() {
    var divCartaJogador = document.getElementById("carta-jogador")
    var moldura = '<img src="https://www.alura.com.br/assets/img/imersoes/dev-2021/card-super-trunfo-transparent.png" style=" width: inherit; height: inherit; position: absolute;">';
    divCartaJogador.style.backgroundImage = `url(${cartaJogador.imagem})`
    var nome = `<p class="carta-subtitle">${cartaJogador.nome}</p>`
    var opcoesTexto = ""

    for (var atributo in cartaJogador.atributos) {
        opcoesTexto += "<input type='radio' name='atributo' value='" + atributo + "'>" + atributo + " " + cartaJogador.atributos[atributo] + "<br>"
    }

    var html = "<div id='opcoes' class='carta-status'>"

    divCartaJogador.innerHTML = moldura + nome + html + opcoesTexto + '</div>'
}

function obtemAtributoSelecionado() {
    var radioAtributo = document.getElementsByName('atributo')
    for (var i = 0; i < radioAtributo.length; i++) {
        if (radioAtributo[i].checked) {
            return radioAtributo[i].value
        }
    }
}

function verificaVezDeJogar(){
    var radioAtributo = document.getElementsByName('atributo')
    if(vezDoJogador){
      alert("Sua vez de escolher um atributo")
      radioAtributo.disabled = false
      vezDoJogador = false      
    }else{
      alert("A máquina escolheu um atributo.")
      var escolhaMaquina = parseInt(Math.random() * 3)
      desabilitaAtributo(radioAtributo)
      radioAtributo[escolhaMaquina].checked = true
      radioAtributo[escolhaMaquina].set
      vezDoJogador = true
    }
}

function desabilitaAtributo(radioAtributo){
  for(var i=0; i< 3; i++){
    radioAtributo[i].disabled = true
  } 
}

function jogar() {
    var divResultado = document.getElementById("resultado")
    var atributoSelecionado = obtemAtributoSelecionado()

    if (cartaJogador.atributos[atributoSelecionado] > cartaMaquina.atributos[atributoSelecionado]) {
        htmlResultado = '<p class="resultado-final">Venceu</p>'
        pontosJogador++
    } else if (cartaJogador.atributos[atributoSelecionado] < cartaMaquina.atributos[atributoSelecionado]) {
        htmlResultado = '<p class="resultado-final">Perdeu</p>'
        pontosMaquina++
    } else {
        htmlResultado = '<p class="resultado-final">Empatou</p>'
    }
 
    divResultado.innerHTML = htmlResultado
    exibeCartaMaquina()
    atualizaPlacar()
    document.getElementById('btnJogar').disabled = true
    document.getElementById('btnProximaRodada').disabled = false
    
    atualizaQuantidadeCartas()
  
    if(cartas.length == 0){
      alert("Fim de jogo")
      if(pontosJogador > pontosMaquina){
        htmlResultado = '<p class="resultado-final">VocÊ Venceu</p>'
      }else if(pontosJogador < pontosMaquina){
        htmlResultado = '<p class="resultado-final">VocÊ Perdeu</p>'     
      }else{
        htmlResultado = '<p class="resultado-final">Empatou</p>'
      }
      divResultado.innerHTML = htmlResultado
      document.getElementById('btnProximaRodada').disabled = true
    }
}

